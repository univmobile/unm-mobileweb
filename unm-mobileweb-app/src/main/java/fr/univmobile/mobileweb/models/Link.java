package fr.univmobile.mobileweb.models;

public class Link {

	private int id;
	private String label;
	private String url;
	private int universityId;
	
	public int getId() {
		return id;
	}
	
	public String getLabel() {
		return label;
	}
	
	public String getUrl() {
		return url;
	}
	
	public int getUniversityId() {
		return universityId;
	}
}
